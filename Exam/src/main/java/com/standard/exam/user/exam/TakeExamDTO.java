package com.standard.exam.user.exam;

public class TakeExamDTO {
	private String seq;
	private String headline;
	private String midline;
	private String footline;
	private String testSubject;
	private String timeLimit;
	private String testPrice;
	
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getHeadline() {
		return headline;
	}
	public void setHeadline(String headline) {
		this.headline = headline;
	}
	public String getMidline() {
		return midline;
	}
	public void setMidline(String midline) {
		this.midline = midline;
	}
	public String getFootline() {
		return footline;
	}
	public void setFootline(String footline) {
		this.footline = footline;
	}
	public String getTestSubject() {
		return testSubject;
	}
	public void setTestSubject(String testSubject) {
		this.testSubject = testSubject;
	}
	public String getTimeLimit() {
		return timeLimit;
	}
	public void setTimeLimit(String timeLimit) {
		this.timeLimit = timeLimit;
	}
	public String getTestPrice() {
		return testPrice;
	}
	public void setTestPrice(String testPrice) {
		this.testPrice = testPrice;
	}
	
	
}
